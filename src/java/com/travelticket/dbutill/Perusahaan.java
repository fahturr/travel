package com.travelticket.dbutill;

/**
 *
 * @author fatur
 */
public class Perusahaan {

    private String idPerusahaan;
    private String namaPerusahaan;
    private String usernamePerusahaan;
    private String passwordPerusahaan;

    public String getIdPerusahaan() {
        return idPerusahaan;
    }

    public void setIdPerusahaan(String idPerusahaan) {
        this.idPerusahaan = idPerusahaan;
    }

    public String getNamaPerusahaan() {
        return namaPerusahaan;
    }

    public void setNamaPerusahaan(String namaPerusahaan) {
        this.namaPerusahaan = namaPerusahaan;
    }

    public String getUsernamePerusahaan() {
        return usernamePerusahaan;
    }

    public void setUsernamePerusahaan(String usernamePerusahaan) {
        this.usernamePerusahaan = usernamePerusahaan;
    }

    public String getPasswordPerusahaan() {
        return passwordPerusahaan;
    }

    public void setPasswordPerusahaan(String passwordPerusahaan) {
        this.passwordPerusahaan = passwordPerusahaan;
    }

}
